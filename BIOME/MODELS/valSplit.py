import random

#######################################################################################################################
## -- utility split function -- #######################################################################################
#######################################################################################################################
def valSplit(data, valSize):
  splitGroupTwo = random.sample(data, int(len(data) * valSize))
  splitGroupOne = [item for item in data if item not in splitGroupTwo]
  return splitGroupOne, splitGroupTwo